# basic mathematical operations available in python
# follows the standard PEMDAS order of operations

friends = 0

# operation: addition
# friends = friends + 5
friends += 5  ## augmented assignment operator

# operation: substraction
# friends = friends - 1
friends -= 1

# operation: multiplication
# friends = friends * 3
friends *= 3

# operation: division
# friends = friends / 2
friends /= 2

# operation: exponent
# friends = friends ** 2
friends **= 2

# operation: modulus
reminder = friends % 8  ## how many remains if friends are divided to groups of 8

print(f"you currently have {friends} friends")
